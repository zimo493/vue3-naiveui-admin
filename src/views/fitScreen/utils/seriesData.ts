/**
 * 获取折线数据
 * @param length 折线数量
 * @returns 折线数据
 */
const getSeriesData = (length: number) => {
  const cityList = city.sort(() => Math.random() - 0.5);

  const moveLines: MoveLine[] = [];

  for (let i = 0; i <= length; i++) {
    const from = cityList[Math.floor(Math.random() * cityList.length)];
    const to = cityList[Math.floor(Math.random() * cityList.length)];

    const [a1, b1] = from.value;
    const [a2, b2] = to.value;

    moveLines.push({
      fromName: from.name,
      toName: to.name,
      coords: [
        [a1, b1],
        [a2, b2],
      ],
    });
  }

  // 通过数量计算出symbolSize的值
  city.forEach((item) => {
    item.symbolSize =
      moveLines.filter((line) => line.fromName === item.name || line.toName === item.name).length *
      2;
  });

  return {
    cityData: city,
    moveLines,
  };
};

export default getSeriesData;
